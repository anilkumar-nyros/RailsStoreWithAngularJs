class Product < ActiveRecord::Base
	attr_accessor :url
	attr_reader :url
	attr_writer :url
 has_attached_file :avatar, :styles => { :medium => "300x300>", :thumb => "100x100>" }, :default_url => "/images/:style/missing.png"
  validates_attachment_content_type :avatar, :content_type => /\Aimage\/.*\Z/
end
